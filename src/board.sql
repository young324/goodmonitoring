	`WORK_END_DATE` DATE NULL DEFAULT NULL,
	`WORK_TIME` VARCHAR(50) NULL DEFAULT NULL,
	`WORK_TEXT` VARCHAR(200) NULL DEFAULT NULL,
	`WORK_BENEFIT_KEY` VARCHAR(50) NULL DEFAULT NULL,
