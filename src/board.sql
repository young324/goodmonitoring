	`AD_ANNOUNCEMENT_DATE` DATE NULL DEFAULT NULL,
	`AD_APPLY_TEXT` VARCHAR(200) NULL DEFAULT NULL,
	`C_EMAIL` VARCHAR(30) NULL DEFAULT NULL,
	`AD_AGE` VARCHAR(30) NULL DEFAULT NULL,
	`AD_CHILD` VARCHAR(20) NULL DEFAULT NULL,
	`AD_AREA_NAME` VARCHAR(100) NULL DEFAULT NULL,
