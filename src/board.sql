CREATE TABLE `board` (
	`BOARD_NO` INT(11) NOT NULL AUTO_INCREMENT,
	`AD_TITLE` VARCHAR(50) NULL DEFAULT NULL,
	`C_NAME` VARCHAR(20) NULL DEFAULT NULL,
	`INTEREST_CATEGORY` VARCHAR(50) NULL DEFAULT NULL,
	`TARGET` VARCHAR(50) NULL DEFAULT NULL,
	`AD_PERSONNEL` CHAR(3) NULL DEFAULT NULL,
	`AD_START_DATE` DATE NULL DEFAULT NULL,
